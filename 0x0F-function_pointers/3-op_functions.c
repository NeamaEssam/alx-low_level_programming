#include"3-calc.h"

/**
* op_add - searches for an integer.
* @a: input
* @b: input
* Return: sum of a, b
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - subtract a, b
* @a: input
* @b: input
* Return: sum of a, b
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - multiply a, b
* @a: input
* @b: input
* Return: mul of a, b
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - division a, b
* @a: input
* @b: input
* Return: div of a, b
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - modulas a, b
* @a: input
* @b: input
* Return: mod of a, b
*/

int op_mod(int a, int b)
{
	return (a % b);
}
